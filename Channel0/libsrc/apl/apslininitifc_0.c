/* Description : LIN interface initialization                           */
/* Parameters  : Nothing                                                */
/* Return value: Nothing                                                */
/* Misc.       : It is another name for LIN API l_ifc_init.             */
/*               This process has the period of suspending interrupts.  */
/* -------------------------------------------------------------------- */
void
ApSLin_vogInitIfc(void)
{
    u1 u1tIrq;


    u1tIrq =  ConfSLin_vogCallDisableIrq();
    /*-----------------------*/
    /*   Disable interrupt   */
    /*                       */

 
    LCoreSCom_vogInitAll();


    /*                       */
    /*   Enable interrupt    */
    /*-----------------------*/
    ConfSLin_vogCallRestoreIrq(u1tIrq);


    return;
}

