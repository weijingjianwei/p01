/* Description : Wake up                                                */
/* Parameters  : Nothing                                                */
/* Return value: Nothing                                                */
/* Misc.       : This function is an alias of LIN API l_ifc_wake_up.    */
/*               This process has the period of suspending interrupts.  */
/* -------------------------------------------------------------------- */
void
ApSLin_vogWakeupIfc(void)
{
    u1 u1tIrq;


    u1tIrq =  ConfSLin_vogCallDisableIrq();
    /*-----------------------*/
    /*   Disable interrupt   */
    /*                       */


    /* The difference of wake-up method is implemented in the LIN core layer */
    LCoreSSleep_vogSendWakeup();


    /*                       */
    /*   Enable interrupt    */
    /*-----------------------*/
    ConfSLin_vogCallRestoreIrq( u1tIrq );
    

    return;
}

